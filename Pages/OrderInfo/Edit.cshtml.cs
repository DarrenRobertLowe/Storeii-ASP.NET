using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using Storeii.Data;
using Storeii.Models;

namespace Storeii.Pages.OrderInfo
{
    public class EditModel : PageModel
    {
        private readonly Storeii.Data.StoreiiContext _context;

        public EditModel(Storeii.Data.StoreiiContext context)
        {
            _context = context;
        }

        [BindProperty]
        public Orders Orders { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            Orders = await _context.Orders
                .Include(o => o.AddressId)
                .Include(o => o.CustomerId)
                .Include(o => o.DriverId)
                .Include(o => o.LocationId).FirstOrDefaultAsync(m => m.Id == id);

            if (Orders == null)
            {
                return NotFound();
            }
           ViewData["Address_Id"] = new SelectList(_context.Address, "Id", "Id");
           ViewData["Customer_Id"] = new SelectList(_context.Customer, "Id", "Id");
           ViewData["Driver_Id"] = new SelectList(_context.Driver, "Id", "Id");
           ViewData["Location_Id"] = new SelectList(_context.Location, "Id", "Id");
            return Page();
        }

        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see https://aka.ms/RazorPagesCRUD.
        public async Task<IActionResult> OnPostAsync()
        {
            if (!ModelState.IsValid)
            {
                return Page();
            }

            _context.Attach(Orders).State = EntityState.Modified;

            try
            {
                await _context.SaveChangesAsync();
            }
            catch (DbUpdateConcurrencyException)
            {
                if (!OrdersExists(Orders.Id))
                {
                    return NotFound();
                }
                else
                {
                    throw;
                }
            }

            return RedirectToPage("./Index");
        }

        private bool OrdersExists(int id)
        {
            return _context.Orders.Any(e => e.Id == id);
        }
    }
}
